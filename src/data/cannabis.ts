// Helper function to convert image number to path
const getImagePath = (imageNumber: number | number[], type: 'weed' | 'mushroom' | 'hash' | 'edible' | 'wax' = 'weed'): string[] => {
  const folder = type === 'mushroom' ? 'mushroom_images_300' : 
                 type === 'hash' ? 'hash_images_300' : 
                 type === 'edible' ? 'edible_images_300' : 
                 type === 'wax' ? 'wax_images_300' :
                 'weed_images_300';
  if (Array.isArray(imageNumber)) {
    return imageNumber.map(num => `/${folder}/DSCF${num}X300.jpg`);
  }
  return [`/${folder}/DSCF${imageNumber}X300.jpg`];
};

// Helper function to parse effects string into structured format
const parseEffects = (effectsStr: string) => {
  const effects = effectsStr.split(',').map(e => e.trim());
  return {
    primary: effects.slice(0, 3),
    secondary: effects.slice(3),
    duration: ['2-4 hours']
  };
};

// Helper function to generate SKU
const generateSKU = (name: string, index: number): string => {
  const prefix = name.substring(0, 3).toUpperCase().replace(/[^A-Z]/g, '');
  return `${prefix}-${String(index).padStart(4, '0')}`;
};

